package org.firstinspires.ftc.teamcode.deltacamera;

// Stores and RGB value
public class RGBAverage
{
    // RGB averages
   public int red;
   public int green;
   public int blue;

    // Constructor
    public RGBAverage(int red, int green, int blue)
    {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
}