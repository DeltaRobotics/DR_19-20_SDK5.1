package org.firstinspires.ftc.teamcode.deltacamera;

public class CameraBox
{
    public int xMin = 0;
    public int xMax = 0;
    public int yMin = 0;
    public int yMax = 0;


    public CameraBox(int xMin, int xMax, int yMin, int yMax)
    {
        this.xMin = xMin;
        this.xMax = xMax;
        this.yMin = yMin;
        this.yMax = yMax;

    }
}
